SELECT 
    SUM(SALEPRICE)
FROM
    PETSALE;
    
SELECT
    SUM(SALEPRICE) AS SUM_OF_SALEPRICE
FROM
    PETSALE;
    
SELECT 
    MAX(QUANTITY)
FROM
    PETSALE;
    
SELECT 
	COUNT(SALEPRICE) AS NUM_ANIMAL,
	SUM(SALEPRICE) AS SUM_OF_SALEPRICE,
    AVG(SALEPRICE) AS AVG_OF_SALESPRICE
FROM
    PETSALE;
    
SELECT 
	COUNT(ANIMAL = 'DOG') AS NUM_DOGS,
	SUM(SALEPRICE / QUANTITY) AS SUM_SALEPRICE_PER_UNIT,
    AVG(SALEPRICE / QUANTITY) AS AVG_SALEPRICE_PER_UNIT
FROM
    PETSALE
WHERE
    ANIMAL = 'Dog';
    
SELECT 
    ROUND(SALEPRICE)
FROM
    PETSALE;
    
SELECT 
    LENGTH(ANIMAL)
FROM
    PETSALE;
    
SELECT 
    UCASE(ANIMAL)
FROM
    PETSALE;
    
SELECT DISTINCT
    (UCASE(ANIMAL)) AS ANIMAL_CAP, COUNT(*) AS NUM_ANIMAL
FROM
    PETSALE
GROUP BY (ANIMAL)
ORDER BY NUM_ANIMAL DESC;
    
SELECT 
    *
FROM
    PETSALE
WHERE
    LCASE(ANIMAL) = 'cat';
    
SELECT 
    DAY(SALEDATE)
FROM
    PETSALE
WHERE
    ANIMAL = 'Cat';
    
SELECT 
    COUNT(*)
FROM
    PETSALE
WHERE
    MONTH(SALEDATE) = '05';
    
SELECT 
    SALEDATE + 3 DAYS
FROM
    PETSALE;
    
SELECT 
    CURRENT_DATE - SALEDATE
FROM
    PETSALE;
